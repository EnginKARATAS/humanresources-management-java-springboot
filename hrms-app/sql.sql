	id INT GENERATED BY DEFAULT AS IDENTITY(INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 214748647 CACHE 1),
	name CHARACTER VARYING(15), 
	CONSTRAINT pk_cities PRIMARY KEY(id),
	CONSTRAINT uc_cities_name UNIQUE(name)
);

CREATE TABLE public.schools(
	id INT GENERATED BY DEFAULT AS IDENTITY(INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 214748647 CACHE 1),
	city_id INT NOT NULL,
	name CHARACTER VARYING(50) NOT NULL,
	CONSTRAINT pk_schools PRIMARY KEY(id),
	CONSTRAINT fk_schools_cities FOREIGN KEY(city_id) REFERENCES public.cities(id)
